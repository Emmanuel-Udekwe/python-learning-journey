# ask for a number (1-7) and print the corresponding day of the week
num = int(input("enter a number (1-7):"))
if num == 1:
    print("monday")
elif num == 2:
    print ("tuesday")  
elif num == 3:
    print ("wednesday")   
elif num == 4:
    print ("thursday")
elif num == 5:
    print ("friday")
elif num == 6:
    print ("saturday")   
elif num == 7:
    print ("sunday")   
else:
    print("invalid day number")      